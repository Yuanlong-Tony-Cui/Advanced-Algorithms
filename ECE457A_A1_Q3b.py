path = [[11, 2]]
MAX_MOVES_ALLOWED = 1000000
deadFlag = False


def point_is_valid(coord):
    # Valid means: 1. Within map + Not blocked, 2. Not the second last point
    not_blocked = (0 <= coord[0] <= 24) and (0 <= coord[1] <= 24) and maze[coord[0]][coord[1]] == 0
    not_returning = (len(path) < 2) or (coord != path[len(path) - 2])
    return not_blocked and not_returning


def take_a_random_move():
    # Collect available options for moving:
    next_possible_points = []
    curr_point = path[len(path) - 1]
    print("current point:", curr_point)
    next_point_1 = [curr_point[0] + 1, curr_point[1]]
    next_point_2 = [curr_point[0] - 1, curr_point[1]]
    next_point_3 = [curr_point[0], curr_point[1] + 1]
    next_point_4 = [curr_point[0], curr_point[1] - 1]
    if point_is_valid(next_point_1):
        next_possible_points.append(next_point_1)
    if point_is_valid(next_point_2):
        next_possible_points.append(next_point_2)
    if point_is_valid(next_point_3):
        next_possible_points.append(next_point_3)
    if point_is_valid(next_point_4):
        next_possible_points.append(next_point_4)

    if len(next_possible_points) == 0:
        print("Nowhere to go. Failed to move further.")
        return False

    # Randomly choose a point from the available ones:
    next_point = next_possible_points[random.randint(0, len(next_possible_points) - 1)]
    path.append(next_point)
    return True


def main_navigation():
    i = 0
    last_move_successful = True
    while i < MAX_MOVES_ALLOWED and last_move_successful:
        last_move_successful = take_a_random_move()
        if path[len(path)-1] == [19, 23]:
            print("Reached E1! Path cost:", len(path))
            break
        elif path[len(path)-1] == [21, 2]:
            print("Reached E2! Path cost:", len(path))
            break
    print("Program terminated.")


main_navigation()
